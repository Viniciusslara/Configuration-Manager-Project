    public void readJTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTableLocacao.getModel();
        modelo.setNumRows(0);
        AluguelDAO aluguelDao = new AluguelDAO();
        
        for(Aluguel aluguel: aluguelDao.read()){
            modelo.addRow(new Object[]{
                aluguel.getId(),
                aluguel.cliente.getCpf(),
                aluguel.veiculo.getModelo(),
                aluguel.veiculo.getPlaca(),
                aluguel.getData(),
                aluguel.getDataentrega(),
                aluguel.getValor(),
                aluguel.getMetodoPag()
            });
            
        }
    }

