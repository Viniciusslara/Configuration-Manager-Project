    private void jButtonExcluir5jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluir5jButtonExcluirActionPerformed
        // TODO add your handling code here:
        if(jTableLocacao.getSelectedRow() != -1){

            Aluguel aluguel = new Aluguel();
            AluguelDAO aluguelDao = new AluguelDAO();
            Connect connect = new Connect();

            String placa = jTableLocacao.getValueAt(jTableLocacao.getSelectedRow(), 3).toString();

            aluguel.setId(Integer.parseInt(jTableLocacao.getValueAt(jTableLocacao.getSelectedRow(), 0).toString()));

            aluguelDao.delete(aluguel);

            readJTable();

            if(connect.getConnection()){
                try{

                    String sql = "UPDATE tb_veiculos SET disponibilidade_veiculo = 'DISPONIVEL' WHERE placa_veiculo = '"+placa+"'";

                    PreparedStatement statement = connect.connection.prepareStatement(sql);
                    ResultSet resultSet = statement.executeQuery();

                    resultSet.close();
                    statement.close();
                    connect.close();

                }catch(SQLException erro){
                    System.out.println(erro.toString());
                }
            }else{
                System.out.println("Erro ao conectar");
            }

        }
        
    }//GEN-LAST:event_jButtonExcluir5jButtonExcluirActionPerformed

